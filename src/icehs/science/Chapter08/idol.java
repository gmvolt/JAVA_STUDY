package icehs.science.Chapter08;

public class idol {

	private String group;
	private String name;
	public idol(String group, String name) {
		super();
		this.group = group;
		this.name = name;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
